using System;
using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class BrightFlaw
{
    public static string PotatoIDGin(double a)
    {
        return Math.Round(a, OliverBarter.RaiseCommit).ToString();
    }
    public static string PotatoIDGin(double a, int digits)
    {
        return Math.Round(a, digits).ToString();
    }

    public static double Raise(double a)
    {
        return Math.Round(a, OliverBarter.RaiseCommit);
    }

}
